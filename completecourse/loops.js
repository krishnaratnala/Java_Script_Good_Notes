//Write a loop that prints numbers from 1 to 10. Use both a for loop and a while loop.
//This is using for loop
function print1to10(){
    for (let i=1;i<=10;i++){
        console.log(i)
    }
}
print1to10();
//This is using while loop


function prints1to10(){
    let i=1;
    while(i<=10){
        console.log(i);
        i+=1
    }
    console.log("this is while loop");
}
prints1to10();

